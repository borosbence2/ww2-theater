// Finnish + Arctic (German 20th Mountain Army) order-of-battle scaffolds for the
// Continuation War (1941–44). Until now the Finnish theatre had no units at all.
//
// Same shape as build-minor-axis.mjs: `units` are the unit definitions (incl. the
// Finnish GHQ + armies and the German mountain army), `divisions` carry the
// `events: [[date, army, hgr]]` roster build-units already consumes. These don't
// derive onto the MAIN front — build-units routes everything Finnish-theatre
// (fi-* + de-h-armee-20, and the Soviet Karelian-front armies already in the
// OOB) onto the FINNISH/ARCTIC front lines via sectors/finnish.json.
//
// Output: data/curated/units/oob/finnish.json   (committed, reviewable)

import { writeFileSync } from 'node:fs';

const FROM = '1941-06-25';
const TO = '1944-09-19'; // Moscow Armistice — Finland leaves the war
const GHQ = 'fi-ghq';
const ARC_FROM = '1941-06-29';
const ARC_TO = '1944-11-15'; // German 20th Mountain Army cleared from the Arctic

// [id, English name, short, echelon, type, country, branch, army, hgr, from, to]
const ROW = [
  // Finnish GHQ (Mannerheim) — theatre top, on the Svir/Karelia line.
  ['fi-ghq', 'Finnish GHQ (Mannerheim)', 'GHQ', 'army-group', 'hq', 'FI', 'Finnish', null, null, FROM, TO],
  // Finnish armies / corps groups.
  ['fi-karelian-army', 'Finnish Karelian Army', 'Karelian A', 'army', 'hq', 'FI', 'Finnish', null, GHQ, FROM, TO],
  ['fi-isthmus', 'Finnish Isthmus Forces', 'Isthmus', 'army', 'hq', 'FI', 'Finnish', null, GHQ, FROM, TO],
  // German 20th Mountain Army (Lapland / Arctic) — its own theatre top.
  ['de-h-armee-20', '20th Mountain Army (Lapland)', '20. Geb', 'army', 'hq', 'DE', 'German', null, null, ARC_FROM, ARC_TO],
];

// Finnish divisions — Karelian Army (East Karelia / Svir) + Isthmus.
const DIV = [
  ['fi-1st-division', '1st Finnish Division', '1 D', 'fi-karelian-army', GHQ, FROM, TO],
  ['fi-5th-division', '5th Finnish Division', '5 D', 'fi-karelian-army', GHQ, FROM, TO],
  ['fi-7th-division', '7th Finnish Division', '7 D', 'fi-karelian-army', GHQ, FROM, TO],
  ['fi-11th-division', '11th Finnish Division', '11 D', 'fi-karelian-army', GHQ, FROM, TO],
  ['fi-17th-division', '17th Finnish Division', '17 D', 'fi-karelian-army', GHQ, FROM, TO],
  ['fi-cavalry-brigade', 'Finnish Cavalry Brigade', 'Cav Bde', 'fi-karelian-army', GHQ, FROM, TO],
  ['fi-2nd-division', '2nd Finnish Division', '2 D', 'fi-isthmus', GHQ, FROM, TO],
  ['fi-3rd-division', '3rd Finnish Division', '3 D', 'fi-isthmus', GHQ, FROM, TO],
  ['fi-4th-division', '4th Finnish Division', '4 D', 'fi-isthmus', GHQ, FROM, TO],
  ['fi-10th-division', '10th Finnish Division', '10 D', 'fi-isthmus', GHQ, FROM, TO],
  ['fi-15th-division', '15th Finnish Division', '15 D', 'fi-isthmus', GHQ, FROM, TO],
  ['fi-18th-division', '18th Finnish Division', '18 D', 'fi-isthmus', GHQ, FROM, TO],
  // German mountain divisions (Arctic, under 20th Mountain Army).
  ['de-2nd-mountain-division', '2nd Mountain Division', '2 Geb', 'de-h-armee-20', null, ARC_FROM, ARC_TO],
  ['de-6th-mountain-division', '6th Mountain Division', '6 Geb', 'de-h-armee-20', null, ARC_FROM, ARC_TO],
  ['de-7th-mountain-division', '7th Mountain Division', '7 Geb', 'de-h-armee-20', null, ARC_FROM, ARC_TO],
];

// Mountain/Jäger ride the infantry branch glyph (no separate "mountain" type).
const ECH_TYPE = (id) => (id.includes('cavalry') ? 'cavalry' : 'infantry');
const ECH = (id) => (id.includes('brigade') ? 'brigade' : 'division');

const units = [];
const divisions = [];

for (const [id, name, short, echelon, type, country, branch, , hgr, from, to] of ROW) {
  units.push({
    id,
    country,
    branch,
    echelon,
    type,
    short,
    names: [{ from, name, aliases: [] }],
    existence: [{ from, to }],
    parents: [],
    positions: [],
    links: {},
    imported: true,
  });
  void hgr;
}

for (const [id, name, short, army, hgr, from, to] of DIV) {
  const country = id.startsWith('de-') ? 'DE' : 'FI';
  units.push({
    id,
    country,
    branch: country === 'DE' ? 'German' : 'Finnish',
    echelon: ECH(id),
    type: ECH_TYPE(id),
    short,
    names: [{ from, name, aliases: [] }],
    existence: [{ from, to }],
    parents: [],
    positions: [],
    links: {},
    imported: true,
  });
  divisions.push({ id, label: name, events: [[from, army, hgr]] });
}

const out = {
  note:
    'Finnish + Arctic (German 20th Mountain Army) OOB scaffolds for the Continuation War ' +
    '(1941–44). Generated by build-finnish.mjs. Representative formations; build-units places ' +
    'them on the FINNISH/ARCTIC front lines (sectors/finnish.json), not the main front.',
  units,
  divisions,
};
writeFileSync('data/curated/units/oob/finnish.json', JSON.stringify(out, null, 1));
console.log(`Wrote ${units.length} Finnish/Arctic units (${divisions.length} divisions) -> data/curated/units/oob/finnish.json`);
